                <ul>
                    <li>Download the app from app store or google play, or enter the website.</li>
                    <li>Add a profile for the person in need of care.</li>
                    <li>Add resource people (family and friends). They will get an e-mail with further information about the application (how to log in and how it works).</li>
                    <li>You and the others can add tasks, recurring or single events.</li>
                    <li>Share the tasks between you.</li>
                </ul>

                <p>Everyday use:</p>
                <ul>
                    <li>See all your tasks.</li>
                    <li>Click the checkbox when the task is completed. The checkbox is visible for everyone in the group, that makes it easier for them to keep track of the completed tasks.</li>
                    <li>If you canâ€™t complete a task, you can send it to one of the others through the application, and ask if they can do it. (They receive the message through their chosen option; SMS, e-mail or push-notification.)</li>
                </ul>
                <p>Other functions:</p>
                <ul>
                    <li>You can add as many user profiles as you want.</li>
                    <li>There are no predesigned tasks in the application, so you can define the tasks to fit your needs.</li>
                    <li>You have the possibility to select time intervals for recurring tasks. For example making dinner is set to repeat once a day, walking the dog three times a day, while vacuuming once a week.</li>
                    <li>You can add deadlines to important tasks and they will automatically appear on the top of your to-do-list.</li>
                </ul>
                <p>Data we will collect:</p>
                <p class="liten">To collect data, we will be using different sensors. Touchscreen for a smartphone or tablet, and a keyboard/mouse for the desktop version. The users of our application will have to implement information about the person in need of care, as well as information about the caretakers. The caretakers will be the ones using the application. They have to specify the tasks that needs to be performed, and share the tasks between the members of the group. This will be synchronized with all devices associated to this group, so the completed tasks will be visible for everyone.</p>
                <p class="liten">The data we will be collecting are implemented by the users. Name, phone number, e-mail address and a picture, are personal information required from the user. Only limited information about the person in need of care is required.</p>

